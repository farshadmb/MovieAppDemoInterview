import Foundation
import UIKit

/* Convenient way to access to the app colors */
extension UIColor {
    
    struct Styles {
        
        static let primary = UIColor.Colors.primary
        static let secondary = UIColor.Colors.secondary
        static let tertiary = UIColor.Colors.tertiary
        
        static let white = UIColor.Colors.white
        static let red = UIColor.Colors.red
        static let blue = UIColor.Colors.blue
        static let green = UIColor.Colors.green
        static let yellow = UIColor.Colors.yellow
    }
}
